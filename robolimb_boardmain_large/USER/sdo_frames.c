UNS8 send_sdo_to_node(UNS8 node_id, const SDO_Frame *frame)
{
    Message tx = {0};

    if (frame == 0 || frame->len > SDO_MAX_DATA_LEN) {
        return 0;
    }

    tx.cob_id = 0x600 + node_id; /* 例如 node_id=1 => 0x601 */
    tx.len = frame->len;
    tx.rtr = 0;
    memcpy(tx.data, frame->data, frame->len);

    return canSend(0, &tx);
}
